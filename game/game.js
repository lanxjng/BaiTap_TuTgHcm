function setData(){
    var data = datas[countQuestion];
    document.getElementById('question-id').innerText="Câu hỏi "+(countQuestion+1);
    document.getElementById('question').innerHTML = `<p>${data['question']}</p>`;
    document.getElementById('answer-a').innerText = data['answer']['a'];
    document.getElementById('answer-b').innerText = data['answer']['b'];
    document.getElementById('answer-c').innerText = data['answer']['c'];
    document.getElementById('answer-d').innerText = data['answer']['d'];
    for(var i=0; i<4;i++){
        setCheckResult(data,i);
    }

}
function setCheckResult(data,i){
    var listAnswerString = "abcd";
    document.getElementById('answer-'+listAnswerString[i]).onclick = function(){
        document.getElementById('answer-'+listAnswerString[i]).style.backgroundColor = 'white';
        document.getElementById('answer-'+listAnswerString[i]).style.color = 'black';
        var checkResult = true;
        if(listAnswerString[i]==data['result']){
            checkResult = true;
        }
        else{
            checkResult = false;
        }
        setTimeout(() => {
            document.getElementById('answer-'+listAnswerString[i]).style.color = 'white';
            if(checkResult){
                document.getElementById('answer-'+listAnswerString[i]).style.backgroundColor = '#33CC00';
            }
            else{
                document.getElementById('answer-'+listAnswerString[i]).style.backgroundColor = 'red';
                document.getElementById('answer-'+data['result']).style.backgroundColor = '#FF9900';
            }
        }, 1000);
    }
}
setData();
